@section('web-script')
    <script>
        function addToCart(productId, quantity, btn) {
            $.ajax({
                type: "POST",
                url: "{{ route('cart.add') }}",
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                data: {
                    productId: productId,
                    quantity: quantity,
                }
            }).done(function(res) {
                const data = res.data.original;
                if (data.success) {
                    notiSuccess(data.success, 'center');
                    getTotalProductInCart();
                } else if (data.error) {
                    notiError(data.error);
                }
            }).fail(function(xhr) {
                if (xhr.status === 401) {
                    window.location.href = "/login";
                } else if (xhr.status === 400 && xhr.responseJSON.errors) {
                    const errorMessages = xhr.responseJSON.errors;
                    for (let fieldName in errorMessages) {
                        notiError(errorMessages[fieldName][0]);
                    }
                } else {
                    notiError();
                }
            }).always(function() {
                btn.prop('disabled', false);
            })
        }

        /**
         * hàm tải danh sách sản phẩm theo danh mục
         */
        function searchProductMenu(page = 1, categoryId = null) {
            $.ajax({
                url: '<?= route('website.home.searchProductByCategory') ?>?page=' + page,
                type: "POST",
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                data: {
                    categoryId: categoryId,
                    paginate: 4,
                    status: 1,
                }
            }).done(function(data) {
                $('#listProducts').html(data);
            }).fail(function() {
                notiError();
            });
        }
        $(document).ready(function() {
            searchProductMenu();
            // tìm kiếm sản phẩm theo danh mục
            $('.category-tab').click(function() {
                let categoryId = $(this).data('category-id');
                $('.category-tab').removeClass('active');
                $(this).addClass('active');
                searchProductMenu(page = 1, categoryId = categoryId ?? null);

            });

            // Add to cart
            $(document).on('click', '.addToCart', function() {
                $(this).prop('disabled', true);
                const productId = $(this).data('product-id');
                addToCart(productId, 1, $(this));
                searchProductMenu();
            });


        })
    </script>
@endsection
